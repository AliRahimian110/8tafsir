function CreateZIndex(){
	var elems = document.getElementsByTagName("*");
	var highest = 0;
	
	for (var i = 0; i < elems.length; i++) {
		var dv=(document.defaultView)?document.defaultView:document.parentWindow;
		var zindex=dv.getComputedStyle(elems[i],null).getPropertyValue("z-index");
		if ((zindex > highest) && (zindex != 'auto')) {
			highest = zindex;
		}
	}
	return highest+1;  
}
